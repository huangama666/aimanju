/*
# 更新按章节扣费的码分设置

## 说明
由于剧本生成和剧本分析功能已改为按章节扣费机制，需要更新系统设置中的默认值和描述，
使其准确反映"每章节"的码分消耗。

## 修改内容

### 1. 剧本生成码分设置
- 旧值：10码分（一次性扣费）
- 新值：1码分（每章节）
- 描述：从"剧本生成消耗的码分数"改为"每章节剧本生成消耗的码分数"

### 2. 剧本分析码分设置
- 旧值：5码分（一次性扣费）
- 新值：1码分（每章节）
- 描述：从"分析剧本消耗的码分数"改为"每章节剧本分析消耗的码分数"

## 影响范围
- 码上做剧本功能：按章节扣费，每章节消耗1码分
- 分析剧本功能：按章节扣费，每章节消耗1码分
- 管理后台：系统设置页面显示更新后的描述

## 注意事项
- 此修改不影响其他功能的码分消耗
- 用户可以在管理后台自定义每章节的码分消耗
*/

-- 更新剧本生成的码分设置
UPDATE system_settings
SET 
  value = '1',
  description = '每章节剧本生成消耗的码分数'
WHERE key = 'script_generation_cost';

-- 更新剧本分析的码分设置
UPDATE system_settings
SET 
  value = '1',
  description = '每章节剧本分析消耗的码分数'
WHERE key = 'filming_analysis_cost';
